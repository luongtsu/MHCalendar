import Foundation
import UIKit

extension UIColor {
    
    static func rgb(_ red: CGFloat, _ green: CGFloat, _ blue: CGFloat, _ alpha: CGFloat = 1) -> UIColor {
        return UIColor.init(red: red, green: green, blue: blue, alpha: alpha)
    }
    
    
}
